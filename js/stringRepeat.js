// Return the string printed input number of times
// Examples:  repeatStr(5, "Hello") // "HelloHelloHelloHelloHello"
// (x) get string (x) printed string on count loop based on user choice
function repeatStr(numRepeat,string){
  for (var i=0; i<numRepeat; i+1){
    printString='';
    printString += string;}
    return printString;
  }
